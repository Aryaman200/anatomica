// scripts/gen-quiz-data.mjs — multi-subject NEET quiz-data bundler (ESM)
//
// Scans the questions/ directory of every NEET subject database, concatenates
// all question objects, and writes a single browser-loadable bundle that
// defines the global `const QUIZ_DATA = [...]`.
//
// The output path and global name are intentionally UNCHANGED from the original
// biology-only bundler so that quiz.html (a plain <script src> include) needs no
// edits — it simply gets Physics and Chemistry questions mixed in alongside
// Biology.
//
// Run once (from repo root):  node scripts/gen-quiz-data.mjs
//
// To add real past-paper questions for any subject, drop schema-valid JSON files
// (each an array of question objects) into that subject's questions/ directory
// and rerun this script. See docs/QUESTIONS.md for the schema.

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');

// Each source: the subject DB folder and the subject value to default when a
// question object is missing an explicit `subject` field.
const SOURCES = [
  { dir: 'neet-biology-db/questions', subject: 'Biology' },
  { dir: 'neet-physics-db/questions', subject: 'Physics' },
  { dir: 'neet-chemistry-db/questions', subject: 'Chemistry' },
];

const OUT_FILE = path.join(repoRoot, 'neet-biology-db', 'quiz-data.js');

let all = [];
const counts = {};

for (const { dir, subject } of SOURCES) {
  const absDir = path.join(repoRoot, dir);
  counts[subject] = 0;

  if (!fs.existsSync(absDir)) {
    console.warn(`  [skip] ${subject}: no directory at ${dir}`);
    continue;
  }

  const files = fs.readdirSync(absDir).filter((f) => f.endsWith('.json'));

  for (const file of files) {
    const raw = fs.readFileSync(path.join(absDir, file), 'utf8');
    let data;
    try {
      data = JSON.parse(raw);
    } catch (err) {
      throw new Error(`Failed to parse ${dir}/${file}: ${err.message}`);
    }
    if (!Array.isArray(data)) {
      throw new Error(`${dir}/${file} must be a JSON array of question objects`);
    }

    for (const q of data) {
      // Ensure every question carries a subject field (default per source dir).
      if (!q.subject) q.subject = subject;
    }

    all = all.concat(data);
    counts[subject] += data.length;
  }
}

// Per-subject counts.
console.log('Bundled NEET quiz questions:');
for (const { subject } of SOURCES) {
  console.log(`  ${subject.padEnd(10)} ${counts[subject]}`);
}
console.log(`  ${'TOTAL'.padEnd(10)} ${all.length}`);

const header =
  '/* Auto-generated by scripts/gen-quiz-data.mjs — do not edit manually. */\n' +
  '/* Regenerate:  node scripts/gen-quiz-data.mjs */\n';

fs.writeFileSync(OUT_FILE, header + 'const QUIZ_DATA = ' + JSON.stringify(all) + ';\n');

console.log(`Written ${all.length} questions to ${path.relative(repoRoot, OUT_FILE)}`);
